#include <stdlib.h>

char	*newtab(int i)
{
	char *res;
	int j;

	j = 0;
	i *= i;
	res = malloc(sizeof(char) * i);
	while (j < i)
	{
		if (j == 16)
			res[j] = '#';
		else
			res[j] = '.';
		j++;
	}
	res[i] = '\0';
	return (res);
}
