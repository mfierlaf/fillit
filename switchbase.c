int *switchbase(int *tetro, int size)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if (tetro[i] > 4)
		{
			tetro[i] -= 4;
			tetro[i] += size;
		}
		i++;
	}
	return (tetro);
}
