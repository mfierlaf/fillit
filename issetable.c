int issetable(char *res, int *tetro, int pos)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		if (res[tetro[i] + pos] != '.')
			j++;
		i++;
	}
	if (j != 0)
		return (1);
	else
		return (0);
}
